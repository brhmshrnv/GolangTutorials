package main

func main() {

	a := 10
	b := 20

	total := a + b

	println("total : ", total)

	total = total - 5
	println("total : ", total)

	total *= 20
	println("total : ", total)

	total = 10 / 2
	println("total : ", total)

	total = a % b
	println("total : ", total)

	total++
	println("total : ", total)

	total--
	println("total : ", total)
}
